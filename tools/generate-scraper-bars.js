#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

// Resolve project root relative to this script
const ROOT = path.resolve(__dirname, '..');

const barsDir = path.join(ROOT, 'data', 'bars');
const scraperBars = {};

if (fs.existsSync(barsDir)) {
  const files = fs.readdirSync(barsDir);
  for (const file of files) {
    if (file.endsWith('.json')) {
      const cityKey = file.replace('.json', '');
      const filePath = path.join(barsDir, file);
      try {
        const content = fs.readFileSync(filePath, 'utf8');
        const bars = JSON.parse(content);
        scraperBars[cityKey] = bars;
      } catch (e) {
        console.error(`Failed to parse ${file}:`, e.message);
      }
    }
  }
} else {
  console.error(`Bars directory not found: ${barsDir}`);
  process.exit(1);
}

if (Object.keys(scraperBars).length === 0) {
  console.error('No bar data found to generate scraper-bars.js');
  process.exit(1);
}

const header = [
  '// Bear Event Scraper Bars Configuration',
  '// This file is generated by tools/generate-scraper-bars.js from data/bars/*.json.',
  '// Do not edit this file directly.',
  '//',
  '// USAGE RESTRICTIONS:',
  '// - This is a pure JavaScript configuration file',
  '// - Must export a default bars configuration object',
  '// - Can be imported in both Scriptable and web environments',
  '// - Keep this file environment-agnostic (no Scriptable or DOM APIs)',
  ''
].join('\n');

const exportBlock = [
  '// Export for different environments',
  '// Scriptable environment',
  "if (typeof module !== 'undefined' && module.exports) {",
  '  module.exports = scraperBars;',
  '}',
  '',
  '// ES6 module environment',
  "if (typeof window === 'undefined' && typeof importModule !== 'undefined') {",
  '  // Scriptable environment - make available for importModule',
  '  scraperBars;',
  "} else if (typeof window !== 'undefined') {",
  '  // Browser environment - attach to window',
  '  window.scraperBars = scraperBars;',
  '}',
  '',
  '// Default export for ES6 modules',
  'scraperBars;',
  ''
].join('\n');

const output = `${header}const scraperBars = ${JSON.stringify(scraperBars, null, 2)};\n\n${exportBlock}`;

// Write only if content changes
function writeIfChanged(filePath, content) {
  if (fs.existsSync(filePath)) {
    const existing = fs.readFileSync(filePath, 'utf8');
    if (existing === content) {
      return false;
    }
  }
  fs.writeFileSync(filePath, content);
  return true;
}

const outputPath = path.join(ROOT, 'scripts', 'scraper-bars.js');
const changed = writeIfChanged(outputPath, output);

if (changed) {
  console.log(`✓ Wrote ${path.relative(ROOT, outputPath)}`);
} else {
  console.log(`⏭️  No change for ${path.relative(ROOT, outputPath)}`);
}
